package properties;

public class Siparis {

    String ID, masaID, siparisAdi, siparisAdet, siparisFiyat;
    private String ascidurum;

    public String getID() {
        return ID;
    }

    public void setID(String ID) {
        this.ID = ID;
    }

    public String getMasaID() {
        return masaID;
    }

    public void setMasaID(String masaID) {
        this.masaID = masaID;
    }

    public String getSiparisAdi() {
        return siparisAdi;
    }

    public void setSiparisAdi(String siparisAdi) {
        this.siparisAdi = siparisAdi;
    }

    public String getSiparisAdet() {
        return siparisAdet;
    }

    public void setSiparisAdet(String siparisAdet) {
        this.siparisAdet = siparisAdet;
    }

    public String getSiparisFiyat() {
        return siparisFiyat;
    }

    public void setSiparisFiyat(String siparisFiyat) {
        this.siparisFiyat = siparisFiyat;
    }

    public String getAscidurum() {
        return ascidurum;
    }

    public void setAscidurum(String ascidurum) {
        this.ascidurum = ascidurum;
    }

}
