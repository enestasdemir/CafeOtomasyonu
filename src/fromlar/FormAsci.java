    public void dataDoldur() {
         DefaultTableModel siparis = new DefaultTableModel();
        siparis.addColumn("ID");
        siparis.addColumn("Siparis Adı");
        siparis.addColumn("Adı");
        siparis.addColumn("Adet");
         try {
             String s="call sipAsci()";
             ResultSet rs=db.baglan().executeQuery(s);
             while (rs.next()) {
                 
                 
                 if (rs.getString(5).equals("1") && rs.getString(6).equals("0")) {
                    siparis.addRow(new Object[]{rs.getString(7),rs.getString(1)+rs.getString(2),rs.getString(3),rs.getString(4)}); 
                 }
 
             }
         } catch (SQLException e) {
             
         }
         jTable1.setModel(siparis);

        
        

    }
